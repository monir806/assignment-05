function makeGreen() {
    if (totalPrice === 2200 || totalPrice > 2200) {
        return;
    } else {
        const A1 = document.getElementById("A1");


        A1.classList.add("bg-[#1DD100]");


        let text = parseFloat(document.getElementById("sitCount").innerText);
        SeatCount = text + 1;
        document.getElementById("sitCount").innerText = SeatCount;


        let leftSeat = parseFloat(document.getElementById("total-sit").innerText);
        left = leftSeat - 1;
        document.getElementById("total-sit").innerText = left;

        let TicketInfo = document.getElementById("sit-A1");
        TicketInfo.classList.remove("hidden");

        const amount = parseFloat(document.getElementById("total-price").innerText);
        totalPrice = SeatCount * 550;
        document.getElementById("total-price").innerText = totalPrice;

        document.getElementById("grand-total").innerText = totalPrice;


    }
}

